#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	index;

	if (!s || !f)
		return ;
	index = -1;
	while (s[++index] != '\0')
		(*f)(index, s + index);
}
